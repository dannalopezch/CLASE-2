public class OperacionesAritmeticas {
    public static void main(String[] args) {
        System.out.println("Operaciones Aritmeticas");
        //Definir variables
        int numero_1 = 4;
        int numero_2 = 30;
        int numero_3 = 23;
        int numero_4 = 5;
        //Operaciones aritmeticas
        int suma = numero_1 + numero_4;
        int resta = numero_2 - numero_3;
        int multiplicacion = suma * resta;

        //Imprimir los resultados
        System.out.println("Numero 1: " + numero_1);
        System.out.println("Numero 2: " + numero_2);
        System.out.println("Numero 3: " + numero_3);
        System.out.println("Numero 4: " + numero_4);
        System.out.println("Suma: " + suma);
        System.out.println("Resta: " + resta);
        System.out.println("Multiplicacion: " + multiplicacion);
    }
}
